/* 
 * A while loop will always evaluate the condition first.
 * A do/while loop will always execute the code in the do{} block first and 
 * then evaluate the condition.
 * A for loop allows you to initiate a counter variable, a check condition, 
 * and a way to increment your counter all in one line.
 * 
 * Use for when known the number of iterations.
 * Use while when not know the number in advance.
 */